<p><strong>How is Savings Account Interest Rates Calculated?</strong></p>
<p>&nbsp;</p>
<p>Savings Bank Account interest will be calculated on the basis of end of day account balance maintained in savings bank account, at the rate of interest as specified by ICICI Bank time to time</p>
<p>&nbsp;</p>
<p>With effective from 19th August 2017, the savings account interest rate applicable would be:</p>
<p>&nbsp;</p>
<p>3.5% for end of day account balance of below ₹50 Lacs</p>
<p>&nbsp;</p>
<p>4% for end of day account balance of ₹ 50 Lacs &amp; above.</p>
<p>&nbsp;</p>
<p>Interest is calculated on a daily basis on the daily closing balance in the Account, at the rate specified by ICICI Bank in accordance with Reserve Bank of India directives. The interest amount calculated is rounded off to the nearest rupee. With effect from March 30, 2016, interest will be paid quarterly in March, June, September and December.</p></div>
